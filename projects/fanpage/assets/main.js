function showGenOne() {
    var x = document.querySelector('.gen-one');
    var y = document.querySelector('.desc-one');
    var z = document.querySelector('.desc');

    var l = document.querySelector('.desc-two');
    var m = document.querySelector('.desc-three');
    var n = document.querySelector('.desc-four');
    var o = document.querySelector('.desc-five');
    var p = document.querySelector('.desc-six');

    var a = document.querySelector('.gen-two');
    var b = document.querySelector('.gen-three');
    var c = document.querySelector('.gen-four');
    var d = document.querySelector('.gen-five');
    var e = document.querySelector('.gen-six');
    if (x.style.display === "none") {
        x.style.display = "block";
        y.style.display = "block";
        z.style.fill = "#ffffff";
        a.style.display = "none";
        b.style.display = "none";
        c.style.display = "none";
        d.style.display = "none";
        e.style.display = "none";
        l.style.display = "none";
        m.style.display = "none";
        n.style.display = "none";
        o.style.display = "none";
        p.style.display = "none";

    } else {
        x.style.display = "none";
        y.style.display = "none";
    }
}
let buttonOne = document.querySelector(".first");
buttonOne.addEventListener("click", showGenOne);

